import React, { useContext } from "react";
import { useNavigate } from "react-router-dom";
import axios from "axios";
import { AuthContext } from "../context/AuthContext";

const Logout = () => {
  const { setIsAuthenticated, setUsername } = useContext(AuthContext);
  const navigate = useNavigate();

  const handleLogout = async () => {
    try {
        await axios.get(`${import.meta.env.VITE_API_BASE_URL}logout/`, { withCredentials: true });

      // 游댳 Borrar la sesi칩n en React
      setIsAuthenticated(false);
      setUsername("");
      localStorage.removeItem("isAuthenticated");
      localStorage.removeItem("username");

      // 游댳 Redirigir al usuario a la p치gina de inicio
      navigate("/");
    } catch (error) {
      console.error("Error al cerrar sesi칩n:", error);
    }
  };

  return (
    <button className="btn my-btn" onClick={handleLogout}>Cerrar Sesi칩n</button>
  );
};

export default Logout;
